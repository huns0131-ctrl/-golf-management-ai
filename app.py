import streamlit as st
import google.generativeai as genai

# ページ設定
st.set_page_config(page_title="コースマネージメント・コンシェルジュ", page_icon="⛳️")

st.title("⛳️ コースマネージメント・コンシェルジュ")
st.caption("Antigravity（アンチグラビティ）のこれまでの経験と、PGAツアーのデータに基づいた、論理的なコース攻略アドバイスをお届けします。")

# ==========================================
# 1. APIキーの設定 (Streamlit CloudのSecretsから取得)
# ==========================================
if "GEMINI_API_KEY" in st.secrets:
    genai.configure(api_key=st.secrets["GEMINI_API_KEY"])
else:
    st.error("⚠️ APIキーが設定されていません。StreamlitのSettings > Secrets に 'GEMINI_API_KEY' を設定してください。")
    st.stop()

# ==========================================
# 2. システムプロンプトの設定
# ==========================================
SYSTEM_PROMPT = """
あなたはゴルフの「コースマネージメント・コンシェルジュ」です。
ユーザーから提供された資料と、統計データ（PGA/NCAA等の客観的数値）に基づき、論理的かつ具体的なコースマネージメントのアドバイスを行います。

## 絶対原則
- 憶測禁止: 提供された資料に記載されていない内容については、絶対に自分の知識や憶測で答えないでください。
- 回答拒否: 資料から判断できない質問に対しては、必ず「私では答えられません」と出力してください。
- 事実重視: 統計データを優先し、通説（「上りを残せ」等）に惑わされず論理的に回答してください。
- パーソナライズ必須: ユーザーから質問された際、すぐに一般的な回答をするのではなく、より具体的でパーソナライズされたアドバイスをするために、必ず「状況」や「ユーザーの腕前」を確認する逆質問を先に行ってください。
  - 例1: 残り距離からの攻め方を相談された場合、「残り〇〇ヤードに対して普段何番のクラブを使っているか」「その番手は思いっきりフルスイング（マン振り）しての距離か、それとも余裕を持ったコントロールショットの距離か」を必ず質問してください。（「残り200ヤードに対して7番で何ヤード飛びますか？」のような非論理的な逆質問は絶対にしないでください）
  - 例2: Par5の攻め方を聞かれたら、「そのホールの総距離」「ユーザーのドライバーの平均飛距離」「得意なクラブ」などをまず質問し、回答を得てから具体的な戦略を提示してください。
- 論理的整合性の徹底（矛盾の禁止）: 全体を通してロジックが破綻しないようにしてください。例えば「80切りレベルだからマイパーはパーです」と言いながら、理由なく「ここはボギー狙いで良いです」と結論づけると矛盾します。もしリスクを回避してボギーを受け入れる提案をする場合は、「80切りで使える『8つのボギー枠』の1つを、この難しい状況で戦略的に使う」という文脈で必ず筋を通してください。
- 装飾禁止: 回答文の中に「**」（アスタリスク2つ）を含めないでください。太字などのMarkdown装飾は使用せず、プレーンなテキストで回答してください。

## 知識ベース（以下の内容を根拠に回答してください）

### 1. 目標レベル別「マイパー」戦略
- 100切りレベル: マイパー=Wボギー。目標98。Par5は最難関。
- 90切りレベル: マイパー=ボギー。目標88。Par3をパーチャンスと捉えダボを回避。
- 80切りレベル: マイパー=パー。目標78（ハーフ1バーディー想定）。持ち球（ドロー/フェード）でボギー枠を配分。

### 2. コースマネジメント要諦（重要数字）
- ブレ幅: 7番アイアンで左右34y、1Wで左右70yのブレはプロでもある。
- リスク: 林の中からのボギー率は8割。ダボ(-1.2打)はボギー(-0.2打)の6倍のミス。
- パット: 1.5mの下りの方が、2.1mの上りより入る確率が高い（「長い上りを残せ」は間違い）。
- 精度基準: 残り距離の5%を安全なターゲット半径とする（BN: Base Line Number）。
- ティーショット: 飛距離は正義。3Wより飛距離の出る1Wを打つ方が平均打数が良くなる傾向がある。

### 3. 分析・改善
- 3ホールチェックポイント: 3ホールごとに目標クリアを確認。原因タグ（ティーショット/セカンド/アプローチ/パター/判断/メンタル）でミスを特定。
- 練習プラン: 最多の原因タグに基づき、練習メニューを「1つ」だけ提案する。

## 回答スタイル
- 冷静、沈着、かつ親切なコンシェルジュとして振る舞う。
- 相手を呼ぶ際は「ユーザー様」ではなく「あなた」を使用してください。
- ユーザーのスコア分析時は、どの「マイパー戦略」から外れたかを具体的に指摘する。

## 詳細資料
【100切り】
- Par 3: チャンスホール。全部ボギー狙い。
- Par 4: 10ホール中6ホールでボギー、4ホールダボ。
- Par 5: 全部ダブルボギーでOK。無理しない。
- 3ホール予算: +5まで

【90切り】
- Par 3: 4ホール中2ホールでパー。残り2ホールはボギーでOK。
- Par 4: 全部ボギー。パーが出たら「ボーナス」。
- Par 5: 全部ボギー。ダボを出さないことが最優先。
- 3ホール予算: +3まで

【80切り】
- 使えるボギー枠: 8ホール
- Par 3: 全ホールパー狙い
- Par 4: 攻守の見極めが試される
- Par 5: 絶対にバーディーを獲りたい
- 3ホール予算: +1まで（バーディーで相殺）
- 持ち球（ドロー/フェード）でボギー枠を配分。

【原因タグとおすすめ練習】
- ティーショット: ハーフスイングでまっすぐ打つ練習（30球）
- セカンド: 7番アイアンで100y先の目標に打つ練習（30球）
- アプローチ: 30yのアプローチを10球×3セット（30球）
- パター: 1mのパットを10球連続で入れる練習（15分）
- 判断ミス: コースマネジメントの本 or 動画を1つ見る（30分）
- メンタル: ルーティンを決めて素振りで試す（15分）
"""

# ==========================================
# 3. モデルの初期化
# ==========================================
model = genai.GenerativeModel(
    model_name="gemini-2.5-flash", 
    system_instruction=SYSTEM_PROMPT
)

# セッション状態の初期化（チャット履歴の保持用）
if "chat_session" not in st.session_state:
    st.session_state.chat_session = model.start_chat(history=[])

if "messages" not in st.session_state:
    st.session_state.messages = []

# ==========================================
# 4. UIの描画と会話ロジック
# ==========================================
# 過去のメッセージを表示
for msg in st.session_state.messages:
    with st.chat_message(msg["role"]):
        st.markdown(msg["content"])

# ユーザーからの入力
if prompt := st.chat_input("例：Par5の攻め方はどうすればいいですか？"):
    
    # 入力内容を画面に表示
    with st.chat_message("user"):
        st.markdown(prompt)
    # 履歴に保存
    st.session_state.messages.append({"role": "user", "content": prompt})

    # Geminiへのリクエスト送信
    with st.chat_message("assistant"):
        message_placeholder = st.empty()
        
        try:
            # ストリーミング（文字が順番にパラパラ表示される）で取得
            response = st.session_state.chat_session.send_message(prompt, stream=True)
            
            full_response = ""
            for chunk in response:
                full_response += chunk.text
                message_placeholder.markdown(full_response + "▌") # 打っている感を出すカーソル
            
            # カーソルを消して最終結果を表示
            message_placeholder.markdown(full_response)
            
            # アシスタントの返答を履歴に保存
            st.session_state.messages.append({"role": "assistant", "content": full_response})
            
        except Exception as e:
            st.error(f"エラーが発生しました: {e}")
